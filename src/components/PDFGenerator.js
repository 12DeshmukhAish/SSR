          revisionNumber: revisionNumber || '1',
          clientName: coverPageData.clientName || 'Client Name Not Set',
          propertyNo: coverPageData.propertyNo || 'Property No Not Set',
          propertyAddress: coverPageData.propertyAddress || 'Property Address Not Set',
          estimateCost: estimateCost,
          sorYear: coverPageData.year || coverPageData.sorYear || new Date().getFullYear().toString(),
          area: workInfo.area || coverPageData.area || '900',
          ssr: coverPageData.year || coverPageData.ssr || workInfo.ssr || ''
