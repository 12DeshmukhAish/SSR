const refreshItemProperties = async (txnItemId, retryCount = 0) => {
  const maxRetries = 3;
  
  try {
    console.log(`Refreshing properties for transaction item: ${txnItemId} (attempt ${retryCount + 1})`);
    
    // FIXED: Use the correct API endpoint as shown in your curl example
    const response = await fetch(`${API_BASE_URL}/txn-item-properties/serchByTxnItemId/${txnItemId}`, {
      headers: {
        "Authorization": `Bearer ${jwtToken}`,
        "Accept": "application/json"
      }
    });
    
    if (response.ok) {
      const properties = await response.json();
      const propertiesArray = Array.isArray(properties) ? properties : [];
      
      console.log(`Successfully fetched ${propertiesArray.length} properties`);
      
      // Only update state if properties are found or if we've tried multiple times
      if (propertiesArray.length > 0 || retryCount >= maxRetries - 1) {
        // Update the item details state
        setItemDetails(prev => ({
          ...prev,
          itemProperties: propertiesArray,
          propertiesStatus: propertiesArray.length > 0 ? 'existing' : 'none'
        }));
        
        return propertiesArray;
      } else if (retryCount < maxRetries - 1) {
        // If no properties found and we haven't exhausted retries, wait and retry
        console.log(`No properties found, retrying in 1 second... (${retryCount + 1}/${maxRetries})`);
        await new Promise(resolve => setTimeout(resolve, 1000));
        return refreshItemProperties(txnItemId, retryCount + 1);
      }
      
      return propertiesArray;
    } else {
      const errorText = await response.text();
      console.error(`Failed to refresh properties:`, response.status, response.statusText, errorText);
      
      // Don't show error toast on first attempt if it's a 404 (might be normal)
      if (response.status !== 404 || retryCount > 0) {
        toast.error(`Failed to refresh properties: ${response.status} ${response.statusText}`);
      }
      
      return [];
    }
  } catch (error) {
    console.error("Error refreshing item properties:", error);
    
    // Only show error toast if this isn't a network timeout on first attempt
    if (retryCount > 0 || !error.message.includes('timeout')) {
      toast.error(`Error refreshing properties: ${error.message}`);
    }
    
    // Retry on network errors
    if (retryCount < maxRetries - 1 && (error.name === 'TypeError' || error.message.includes('fetch'))) {
      console.log(`Network error, retrying in 1 second... (${retryCount + 1}/${maxRetries})`);
      await new Promise(resolve => setTimeout(resolve, 1000));
      return refreshItemProperties(txnItemId, retryCount + 1);
    }
    
    return [];
  }
};
