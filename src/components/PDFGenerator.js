          companyLogo: coverPageData.companyLogo || null,
          companyName: coverPageData.companyName || 'Company Name Not Set',
          companyAddress: coverPageData.address || coverPageData.companyAddress || 'Address Not Set',
          workName: coverPageData.workName || nameOfWork || 'Work Name Not Set',
